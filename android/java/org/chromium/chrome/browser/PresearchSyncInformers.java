public class PresearchSyncInformers {
    public static void show() {
        showSetupV2IfRequired();
    }

    private static void showSetupV2IfRequired() {
        if (null == PresearchSyncReflectionUtils.getSyncWorker()) return;

        PresearchSyncWorker presearchSyncWorker = (PresearchSyncWorker)PresearchSyncReflectionUtils.getSyncWorker();
        boolean wasV1User = presearchSyncWorker.getSyncV1WasEnabled();
        if (!wasV1User) {
            return;
        }

        boolean infobarDismissed = presearchSyncWorker.getSyncV2MigrateNoticeDismissed();
        if (infobarDismissed) {
            return;
        }

        boolean isV2User = ProfileSyncService.get() != null && ProfileSyncService.get().isFirstSetupComplete();
        if (isV2User) {
            presearchSyncWorker.setSyncV2MigrateNoticeDismissed(true);
            return;
        }

        showSyncV2NeedsSetup();
    }

    public static void showSyncV2NeedsSetup() {
        PresearchActivity activity = PresearchActivity.getPresearchActivity();
        if (activity == null) return;

        Tab tab = activity.getActivityTabProvider().get();
        if (tab == null) return;

        PresearchSimpleConfirmInfoBarBuilder.createInfobarWithDrawable(tab.getWebContents(),
                new SimpleConfirmInfoBarBuilder.Listener() {
                    @Override
                    public void onInfoBarDismissed() {
                    }

                    @Override
                    public boolean onInfoBarButtonClicked(boolean isPrimary) {
                        if (isPrimary) {
                            SettingsLauncher settingsLauncher = new SettingsLauncherImpl();
                            settingsLauncher.launchSettingsActivity(
                                ContextUtils.getApplicationContext(),
                                PresearchSyncScreensPreference.class);
                        }
                        return false;
                    }

                    @Override
                    public boolean onInfoBarLinkClicked() {
                        return false;
                    }
                },
                // must be SYNC_V2_MIGRATE_INFOBAR_DELEGATE, but now it is introduced through
                // src/presearch/chromium_src/components/infobars/core/infobar_delegate.h and
                // java enums are generated by //components/infobars/core:infobar_generated_enums
                // who does not understand `presearch/chromium_src`
                InfoBarIdentifier.INLINE_UPDATE_READY_INFOBAR_ANDROID,
                activity,
                R.drawable.sync_icon /* drawableId */,
                activity.getString(R.string.presearch_sync_v2_migrate_infobar_message) /* message */,
                activity.getString(R.string.presearch_sync_v2_migrate_infobar_command) /* primaryText */,
                null /* secondaryText */, null /* linkText */, false /* autoExpire */);
        PresearchSyncWorker presearchSyncWorker = (PresearchSyncWorker)PresearchSyncReflectionUtils.getSyncWorker();
        presearchSyncWorker.setSyncV2MigrateNoticeDismissed(true);
    }
}
